
import { createSlice, configureStore } from '@reduxjs/toolkit'

const initialState = {
    counter: 0,
    showCounter: true
}

const counterSlice = createSlice({
    name: 'counter',
    initialState,
    reducers: {
        increment(state) {
            console.log(state.counter)
            state.counter++
         },
        increase(state, action) {
            state.counter = state.counter + action.payload
         },
        decrement(state) {
            state.counter--
         },
        toggle(state) {
            state.showCounter = !state.showCounter
         }
    }
})


const store = configureStore({
    reducer: counterSlice.reducer
})

export const counterActions = counterSlice.actions //contain all the action creators automatically generated by Redux Toolkit for the counterSlice

export default store //connect react application to the store